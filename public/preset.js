const f_enc = function (input, key) {
    try {
        const keyDigits = key.split('').map(Number);
        return input
            .split('')
            .map((char, index) => {
                const charCode = char.charCodeAt(0);
                const shift = keyDigits[(index + index) % 8];
                return String.fromCharCode((charCode ^ shift) ^ 2025);
            })
            .join('');
    } catch {}
}
